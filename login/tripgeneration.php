<span class="title">Classification of Trips</span>
<p class="section">It has been found that better trip generation models can be obtained if the trips by different purpose are identified and modelled separately.The trips can be classified as given below:</p>
<p class="section"><b>1. Home Based Trip: One of the trip end is home.</b></p>
<p class="section"><b>Example:</b> A trip from home to office.</p>
<p class="section">Following are the list of home based trips that is trip purpose which are classified into five categories:</p>
<p class="section">a. Work Trips</p>
<p class="section">b. School Trips</p>
<p class="section">c. Shopping Trips</p>
<p class="section">d. Social- recreational Trips</p>
<p class="section">e. Other Trips</p>
<p class="section">The first two trips are mandatory trips while other trips are discretional trips. The other trip class encompasses all the trips made for less routine purpose such as health bureaucracy etc.</p>
<br>
<p class="section"><b>2. Non Home based trips: None of the trip end is home.</b></p>
<p class="section"><b>Example:</b> A trip from office to Shopping Mall.</p>
<br>
<p class="section"><b>3. Time based trips</b></p>
<p class="section">The proportion of journey is different by different purposes usually varies with time of the day. Thus the classification is often given as Peak and Off Peak Period Trip.</p>
