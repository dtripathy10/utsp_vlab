<span class="title">Summary</span>
<p class="section">The purpose of trip generation is to estimate the number of trip ends for each zones for the targeted year. The trip end is calculated for different travel purpose within the zone. These trips are represented as residential trip production obtained from household based cross classification tables or non residential trip attractions which is obtained by projection of land use. Trip generation Models that are often used are Multiple Linear Regression Model or Cross Classification Model or involves combination of both.</p>
