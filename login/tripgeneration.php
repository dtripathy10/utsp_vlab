<span class="title">Modelling Trip Production</b></span>
<p class="section">Modelling basically relates the dependent variable ie trips produced by a zone for aggregated model or household trip production rate for household based models to the corresponding Independent variables characterised by the whole zone or household characteristic respectively. Calibration is done based on the set of observations obtained corresponding to the zones for aggregate model and for disaggregate model  employs a number of base year observations corresponding to an individual household in a sample of household drawn randomly from the region.<br>
Thus we first need to identify what are the relevant variables:<br>
a. Home end <br>
b. Work End<br>
c. Shop End<br></p>
