<h1 class="designation">Trip Generation</h1>
<span class="title">Introduction</span>
 <p class="section">Trip Generation is the first step in the Sequential Demand Modelling arrangement which is also called as the Four Step Transportation Planning Process(FSTP) as mentioned earlier. In order to carry out modelling, the variable consists of total number of person-trips generated by a zone as a dependent variable and the independent variable consists of household and socio- economic factors which influence the trip making behaviour of the person. The data for the independent variable should be attained from an analyst. The output thus obtained consists of trip making or trip ends for each zone within a region.</p>
 <p class="section">In contemporary transportation planning language, A Trip is defined as a one way person movement by a mechanized mode of transport, having two trip ends. The start of the trip is called as origin and the end of trip is called as destination.  Trip is classified as Production or Origin and Attraction or Destination. It should be note that the terminologies used are not identical. To understand with an example consider a single worker on a typical working day making a trip from his house which is in zone <b>P</b>  to his office in Zone <b>Q</b>. Thus his trip origin will be zone <b>P</b> and trip destination will be zone <b>Q</b>. For the return trip from office to house his trip origin will be zone <b>Q</b> and trip Destination will be Zone <b>P</b>.  Thus from the above Example it can be understood that the term Origin and Destination are defined in terms of direction of the trip while Production and Attraction in terms of land use associated with each trip end. Trip Production is the home end of home based trip and is the origin of trip of non home based trip. Trip Attraction is the non home end of home based trip and is the destination of a non home based trip.</p>
